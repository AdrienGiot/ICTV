from sqlobject import SQLObject, IntCol


class ICTVObject(SQLObject):
    def to_dictionary(self, attributes):
        d = {}
        for attr in attributes:
            d[attr] = self.__getattribute__(attr)
        return d


class DBVersion(ICTVObject):
    version = IntCol()
